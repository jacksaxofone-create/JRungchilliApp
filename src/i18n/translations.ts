export const tSingle = (key: keyof typeof translations.th, lang: Language): string => {
  return translations[lang]?.[key] ?? translations.th[key] ?? key;
};

export const tBilingual = (key: keyof typeof translations.th, primaryLang: Language, secondaryLang: Language): string => {
  const primary = translations[primaryLang]?.[key] ?? translations.th[key] ?? key;
  if (primaryLang === secondaryLang) return primary;
  const secondary = translations[secondaryLang]?.[key] ?? '';
  return secondary ? `${primary} | ${secondary}` : primary;
};